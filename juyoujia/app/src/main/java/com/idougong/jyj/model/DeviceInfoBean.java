package com.idougong.jyj.model;

public class DeviceInfoBean {
    private String systemVersion;
    private String systemModel;
    private String deviceBrand;
    private String deviceImsi;
    private String deviceImei;
    private String myScreenWidth;
    private String myScreenHeight;

    public String getSystemVersion() {
        return systemVersion;
    }

    public void setSystemVersion(String systemVersion) {
        this.systemVersion = systemVersion;
    }

    public String getSystemModel() {
        return systemModel;
    }

    public void setSystemModel(String systemModel) {
        this.systemModel = systemModel;
    }

    public String getDeviceBrand() {
        return deviceBrand;
    }

    public void setDeviceBrand(String deviceBrand) {
        this.deviceBrand = deviceBrand;
    }

    public String getDeviceImsi() {
        return deviceImsi;
    }

    public void setDeviceImsi(String deviceImsi) {
        this.deviceImsi = deviceImsi;
    }

    public String getDeviceImei() {
        return deviceImei;
    }

    public void setDeviceImei(String deviceImei) {
        this.deviceImei = deviceImei;
    }

    public String getMyScreenWidth() {
        return myScreenWidth;
    }

    public void setMyScreenWidth(String myScreenWidth) {
        this.myScreenWidth = myScreenWidth;
    }

    public String getMyScreenHeight() {
        return myScreenHeight;
    }

    public void setMyScreenHeight(String myScreenHeight) {
        this.myScreenHeight = myScreenHeight;
    }
}
